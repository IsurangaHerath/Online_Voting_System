-- =============================================================================
-- Views for common queries
-- =============================================================================

-- View: Get voting results with candidate details
CREATE OR REPLACE VIEW voting_results AS
SELECT 
    c.id AS candidate_id,
    c.name AS candidate_name,
    c.party,
    COUNT(v.id) AS vote_count
FROM candidates c
LEFT JOIN votes v ON c.id = v.candidate_id
WHERE c.is_active = TRUE
GROUP BY c.id, c.name, c.party
ORDER BY vote_count DESC;

-- View: Get all verified voters
CREATE OR REPLACE VIEW verified_voters AS
SELECT 
    id,
    username,
    email,
    created_at
FROM users
WHERE role = 'voter' AND is_verified = TRUE;

-- =============================================================================
-- Stored Procedure: Check if voter has voted
-- =============================================================================
DELIMITER //
CREATE PROCEDURE IF NOT EXISTS has_voted(IN voter_id INT)
BEGIN
    SELECT COUNT(*) AS vote_count FROM votes WHERE voter_id = voter_id;
END //
DELIMITER ;

